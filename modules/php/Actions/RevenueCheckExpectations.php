  public function stRevenueCheckExpectations()
  {
    $balance = Company::getBalance();
    $standing = Company::getStanding();

    if ($balance < $standing) {
      Notifications::message(clienttranslate('The Company does not meet Expectations'));
      Company::adjustStanding(-1);
    } else {
      Notifications::message(clienttranslate('The Company meets Expectations'));
    }


    $this->resolveAction(['automatic' => true]);
  }


